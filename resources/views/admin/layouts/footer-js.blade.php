    $(document).on('keyup', '.price', function () {
        let x = $(this).val();
        x = x.replace('.', '');
        console.log(x)
        if (isNaN(x)) {
            swal({
                title: "Thông báo!",
                text: "Giá trị nhập vào phải là kiểu số",
                icon: "warning",
                buttons: true,
                dangerMode: true,
            });
            $(this).val('')
            return;
        }
        $(this).val(addCommas(x))
    })
    function addCommas(nStr)
    {
        nStr += '';
        x = nStr.split(',');
        x1 = x[0];
        x2 = x.length > 1 ? ',' + x[1] : '';
        var rgx = /(\d+)(\d{3})/;
        while (rgx.test(x1)) {
            x1 = x1.replace(rgx, '$1' + '.' + '$2');
        }
        return x1 + x2;
    }
